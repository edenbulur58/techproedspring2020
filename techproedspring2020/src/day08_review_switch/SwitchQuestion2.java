package day08_review_switch;

public class SwitchQuestion2 {

	public static void main(String[] args) {
		
		String num="Even";
		
		
		switch(num) {
		case "Even":
		System.out.println("Even integer");
		break;
		case "Odd":
			System.out.println("Odd integer");	
       break;
       default:
    	   System.out.println("Enter a valid data");   

	}
	}
}
		
		
		
		
		
		
	
