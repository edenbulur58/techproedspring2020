package day01_variables;

public class Variables2 {

	public static void main(String[] args) {
		
		byte heightOfTree=87;
		byte heightOfBuilding=80;
		System.out.println(heightOfTree-heightOfBuilding);
		
	short sh1=2000;
	short sh2=-5900;
	System.out.println(sh1*sh2);
		
	long num1=34578633;
	long num2=1234567;
	System.out.println(num1+num2);
	
	
	float fl1=1.234567f;
	float fl2=-0.123f;
	System.out.println(fl1*fl2);
	
	
	double dbl1=123456789;
	double dbl2=2.3456789;
	System.out.println(dbl1-dbl2);
	
	
	int num3=Integer.MIN_VALUE;
	System.out.println(num3);
	
	int num4=Integer.MAX_VALUE;
	System.out.println(num4);
	
	byte num5=Byte.MAX_VALUE;
	System.out.println("Max value of byte"+num5);
	
	byte num6=Byte.MIN_VALUE;
	System.out.println("Min value of byte"+num6);
	
	short sh4=Short.MAX_VALUE, sh5=Short.MIN_VALUE;
	System.out.println("Max value of byte"+sh4);
	System.out.println("Min value of byte"+sh5);
	
	float fl3=Float.MAX_VALUE, fl4=Float.MIN_VALUE;
	System.out.println("Max value of float"+fl3);
	System.out.println("Min value of float"+fl4);
	
	double dbl3=Double.MAX_VALUE, dbl4=Double.MIN_VALUE;
	System.out.println("Max value of double"+dbl3);
	System.out.println("Min value of double"+dbl4);
	
	
	
	
	}

}
